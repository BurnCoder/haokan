            $(function() {
                var sex = 1;
                loadCategories(sex);
                $(".c_list_tab_box > a").click(function() {
                    $(this).addClass("hover");
                    $(this).siblings().removeClass("hover");
                    var sex = $(this).attr("sex");
                    loadCategories(sex);
                });
            })

            function loadCategories(sex) {
                var opposite = (sex == 1) ? 2 : 1;
                $(".sex_" + opposite).hide();
                if (!$(".sex_" + sex).length) {
                    makeCategoryHtml(sex);
                }
                $(".sex_" + sex).show();
            }

            function makeCategoryHtml(sex) {
                $.get("/api/category/channelcategoriesapi", {
                    'sex': sex
                },
                function(data) {
                    $.each(data,
                    function(key, value) {
                        var imgList = ["https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_1.png?v=2.1.35", "https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_2.png?v=2.1.35", "https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_3.png?v=2.1.35", "https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_4.png?v=2.1.35", "https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_5.png?v=2.1.35", "https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_6.png?v=2.1.35", "https://cpsn.klmnf.com/assets/qy/img/frontend/banner/classify_icon_7.png?v=2.1.35"];
                        var simg = imgList[(key % 7)];
                        if (value.image) {
                            simg = value.image;
                        }
                        var item = '<li class="sex_' + sex + '">';
                        item += '<a href="/index/category/books?book_category_id=' + value.id + '" category-id="' + value.id + '">';
